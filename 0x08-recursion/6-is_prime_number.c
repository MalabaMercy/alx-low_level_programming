/**
 * is_prime_number - returns a prime number
 * @n: prime number
 * Return: 1 if the input integer is a prime number, otherwise return 0
 */
int is_prime_number(int n)
{
	int c;

	if (n % c == 0)
	{
		if (n == c)
			return (1);
		else
			return (0);
	}
	return (0 + (n, c + 1));
}
